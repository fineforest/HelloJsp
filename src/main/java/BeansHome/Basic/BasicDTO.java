import Common.ExceptionMgr;

// ═════════════════════════════════════════════════════════════════════════════════════════
// 사용자정의 클래스 영역
// ═════════════════════════════════════════════════════════════════════════════════════════
/***********************************************************************
 * BasicDTO		: 기본 DTO 클래스<br>
 * Inheritance	: None
 ***********************************************************************/
public class BasicDTO
{
	// —————————————————————————————————————————————————————————————————————————————————————
	// 전역상수 관리 - 필수영역
	// —————————————————————————————————————————————————————————————————————————————————————
	
	// —————————————————————————————————————————————————————————————————————————————————————
	// 전역변수 관리 - 필수영역(정적변수)
	// —————————————————————————————————————————————————————————————————————————————————————
    
	// —————————————————————————————————————————————————————————————————————————————————————
	// 전역변수 관리 - 필수영역(인스턴스변수)
	// —————————————————————————————————————————————————————————————————————————————————————
	/** txtData1	: Bean 전역 변수1 필드 선언(JSP 입력 객체와 1:1 대응, 소문자로 시작) */
	private String txtData1 = null;
	/** txtData2	: Bean 전역 변수2 필드 선언(JSP 입력 객체와 1:1 대응, 소문자로 시작) */
	private String txtData2 = null;
	
	/** oArrayValue	: Bean 전역 변수3 선언(전역변수를 JSP에서 사용하기 위해) */
	public int[] oArrayValue = { 1, 2, 3, 4, 5};
	// —————————————————————————————————————————————————————————————————————————————————————
    // 생성자 관리 - 필수영역(인스턴스함수)
	// —————————————————————————————————————————————————————————————————————————————————————
	/***********************************************************************
	 * BasicDTO()	: 생성자
	 * @param void	: None
	 ***********************************************************************/
	public BasicDTO()
	{
		try
		{
			// -----------------------------------------------------------------------------
			// 기타 초기화 작업 관리
			// -----------------------------------------------------------------------------
			ExceptionMgr.SetMode(ExceptionMgr.RUN_MODE.DEBUG);
			// -----------------------------------------------------------------------------
		}
		catch (Exception Ex)
		{
			ExceptionMgr.DisplayException(Ex);		// 예외처리(콘솔)
		}
    }
	// —————————————————————————————————————————————————————————————————————————————————————
	// 전역함수 관리 - 필수영역(정적함수)
	// —————————————————————————————————————————————————————————————————————————————————————
	
	// —————————————————————————————————————————————————————————————————————————————————————
	// 전역함수 관리 - 필수영역(인스턴스함수)
	// —————————————————————————————————————————————————————————————————————————————————————
	public String getTxtData1()
	{
		return txtData1;
	}

	public void setTxtData1(String txtData1)
	{
		this.txtData1 = txtData1;
	}

	public String getTxtData2()
	{
		return txtData2;
	}

	public void setTxtData2(String txtData2)
	{
		this.txtData2 = txtData2;
	}
	
	/***********************************************************************
	 * HelloCheck()	: Bean 전역 함수 선언(함수를 JSP에서 호출하기 위해)
	 * @param void	: None
	 * @return void	: None
	 ***********************************************************************/
	public String HelloCheck()
	{
		String sResult = null;
		
		try
		{
			sResult = String.format("txtData1 : %s, txtData2 : %s", this.txtData1, this.txtData2);
		}
		catch(Exception Ex)
		{
			Common.ExceptionMgr.DisplayException(Ex);		// 예외처리(콘솔)
		}
			
		return sResult;
	}
	// —————————————————————————————————————————————————————————————————————————————————————
}
